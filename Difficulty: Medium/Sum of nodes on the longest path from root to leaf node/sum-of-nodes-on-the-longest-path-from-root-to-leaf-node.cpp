class Solution
{
public:
    // Helper function to find the longest path and the sum of nodes on that path
    void findLongestPath(Node* root, int length, int sum, int &maxLength, int &maxSum) {
        // Base case: if the node is null
        if (root == NULL) {
            // If the current path length is greater than the maxLength, update maxLength and maxSum
            if (length > maxLength) {
                maxLength = length;
                maxSum = sum;
            }
            // If the current path length is equal to the maxLength, update maxSum only if sum is greater
            else if (length == maxLength) {
                maxSum = std::max(maxSum, sum);
            }
            return;
        }

        // Add the current node's value to sum and increase the length
        sum += root->data;
        length++;

        // Recursively go left and right
        findLongestPath(root->left, length, sum, maxLength, maxSum);
        findLongestPath(root->right, length, sum, maxLength, maxSum);
    }

    int sumOfLongRootToLeafPath(Node *root) {
        int maxLength = 0; // To store the maximum length of any path
        int maxSum = 0;    // To store the maximum sum of the longest path
        findLongestPath(root, 0, 0, maxLength, maxSum);
        return maxSum;
    }
};

